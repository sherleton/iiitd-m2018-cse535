package com.example.nikhi.project;

public class user {
    public String appointment;
    public String name;
    public String password;
    public String phone;
    public String roll;
    public String type;

    public user(String appointment, String name, String password, String phone, String roll, String type) {
        this.appointment = appointment;
        this.name = name;
        this.password = password;
        this.phone = phone;
        this.roll = roll;
        this.type = type;
    }
}
