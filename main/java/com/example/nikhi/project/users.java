package com.example.nikhi.project;

public class users {
    public boolean appointment;
    public String name;
    public String password;
    public int phone;
    public String roll;
    public String type;
    public String username;

    public users(boolean appointment, String name, String password, int phone, String roll, String type, String username) {
        this.appointment = appointment;
        this.name = name;
        this.password = password;
        this.phone = phone;
        this.roll = roll;
        this.type = type;
        this.username = username;
    }
}
