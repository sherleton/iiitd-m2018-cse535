package com.example.nikhi.project;

public class appointments {
    String accept;
    String date;
    String time;
    String description;
    String roll;
    String name;

    public appointments(String accept, String date, String time, String description, String roll, String name) {
        this.accept = accept;
        this.date = date;
        this.time = time;
        this.description = description;
        this.roll = roll;
        this.name = name;
    }
}
