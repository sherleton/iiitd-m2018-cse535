package com.example.nikhi.project;

public class medreq {
    public String accept;
    public String medicine;
    public String name;
    public String qty;
    public String roll;
    public String user;

    public medreq(String accept, String medicine, String name, String qty, String roll, String user) {
        this.accept = accept;
        this.medicine = medicine;
        this.name = name;
        this.qty = qty;
        this.roll = roll;
        this.user = user;
    }
}
